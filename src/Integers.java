public class Integers {
    public static void main(String[] args) {
        byte byteNum = 100;
        System.out.println(byteNum);
        short shortNum = 5000;
        System.out.println(shortNum);
        int intNum = 100_000;
        System.out.println(intNum);
        long longNum = 15_000_000_000L;
    }
}
