public class FloatAndDouble {
    public static void main(String[] args) {
        float floatNum = 5.75f;
        System.out.println(floatNum);
        double doubleNum = 19.99d;
        System.out.println(doubleNum);
        //scientific number
        float f1 = 35e3f;
        double d1 = 12E4d;
    }
}
